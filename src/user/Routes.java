package user;

public class Routes {

	private int id;
	private String name;
	private String ipAddress;
	private String systemId;
	private String password;
	private String txPort;
	private String rxPort;
	private String txRxPort;
	private String systemType;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIpAddress() {
		return ipAddress;
	}
	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}
	public String getSystemId() {
		return systemId;
	}
	public void setSystemId(String systemId) {
		this.systemId = systemId;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getTxPort() {
		return txPort;
	}
	public void setTxPort(String txPort) {
		this.txPort = txPort;
	}
	public String getRxPort() {
		return rxPort;
	}
	public void setRxPort(String rxPort) {
		this.rxPort = rxPort;
	}
	public String getTxRxPort() {
		return txRxPort;
	}
	public void setTxRxPort(String txRxPort) {
		this.txRxPort = txRxPort;
	}
	public String getSystemType() {
		return systemType;
	}
	public void setSystemType(String systemType) {
		this.systemType = systemType;
	}
	
	
	
}
